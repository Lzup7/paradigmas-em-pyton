n_inteiro1 = int(input("informe o 1º numero inteiro: "))
n_inteiro2 = int(input("informe o 2º numero inteiro: "))
n_real = float(input("informe o numero real: "))

solução1 = (n_inteiro1 * 2) * (n_inteiro2 / 2)
print(f"O dobro do primeiro multiplicado pela metade do segundo é {solução1:.0f}")
solução2 = (3 * n_inteiro1) + n_real
print(f"A soma do triplo do primeiro com o terceiro é {solução2:.2f}")
solução3 = n_real**3
print(f"O terceiro numero elevado ao cubo é {solução3:.2f}")
